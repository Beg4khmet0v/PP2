i = 0
while i < 6:
  i += 1
  if i == 3:
    continue
  print(i) #this is example of while loop with continue statement from w3schools

j = 10
while j > 0:
  j -= 2
  if j == 6:
    continue
  print(j) #1st example

k = 0
while k <= 5:
    k += 1
    if k == 3:
        continue
    print(k) #2nd example

l = 5
while l >= 0:
    l -= 1
    if l == 2:
        continue
    print(l) #3rd example

m = 1
while m < 10:
    m *= 2
    if m == 4:
        continue
    print(m) #4th example

n = 100
while n > 0:
    n //= 2
    if n == 25:
        continue
    print(n) #5th example