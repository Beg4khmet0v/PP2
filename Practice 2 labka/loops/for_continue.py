fruits = ["apple", "banana", "cherry"]
for x in fruits:
  if x == "banana":
    continue
  print(x) #this is example of for loop with continue statement from w3schools

numbers = [1, 2, 3, 4, 5]
for num in numbers:
  if num == 3:
    continue
  print(num) #1st example

colors = ["red", "green", "blue"]
for color in colors:
  if color == "green":
    continue
  print(color) #2nd example

pets = ["dog", "cat", "rabbit"]
for pet in pets:
    if pet == "cat":
        continue
    print(pet) #3rd example

cities = ["New York", "Los Angeles", "Chicago"]
for city in cities:
    if city == "Los Angeles":
        continue
    print(city) #4th example

